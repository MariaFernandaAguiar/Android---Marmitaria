    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(usu.isEmpty())
        {
        txtId.setText("");
        txtNome.setText("");
        txtEndereco.setText("");
        txtCelular.setText("");
        txtSenha.setText("");
        grupoStatus.clearSelection();
        
        }else
        {
            int opcao = JOptionPane.showConfirmDialog(null, "Tem ceteza que deseja excluir o usuario"+txtNome.getText(),"Confirmação",JOptionPane.YES_NO_OPTION);
            if(opcao ==0)
            {
                dao.excluirUsuario(Integer.parseInt(txtId.getText()));
                usu.clear();
                usu = dao.getUsuarios();
                indice=usu.size()-1;
                mostrarDados();
                
            }
            else
            JOptionPane.showMessageDialog(null, "Não foi excluído");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


