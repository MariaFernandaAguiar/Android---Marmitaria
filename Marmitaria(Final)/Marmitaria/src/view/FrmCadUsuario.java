    private void btnPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimeiroActionPerformed
        if(usu.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Não exitem usuarios cadastrados");
        }
        else
        {
            indice=0;
            mostrarDados();
        }
    }//GEN-LAST:event_btnPrimeiroActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        if(usu.isEmpty()){
                JOptionPane.showMessageDialog(null, "Não existe usuario cadastrados");
        }
        else{
            indice--;
        }
        if(indice<0){
            indice++;
            JOptionPane.showMessageDialog(null, "Você já esta no primeiro registro");
        }
        mostrarDados();
        btnProximo.setEnabled(true);
    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        if(usu.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Não exitem usuario cadastrados");
        }
        else
        {
            indice++;
            if(indice>usu.size()-1)
            {
                indice--;
                JOptionPane.showMessageDialog(null, "Você já está no último registro");
            }
            mostrarDados();
        }
    }//GEN-LAST:event_btnProximoActionPerformed

    private void btnUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUltimoActionPerformed
        if(usu.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Não exitem usuariobooks cadastrados");
        }
        else
        {
            indice=usu.size()-1;
            mostrarDados();
            btnProximo.setEnabled(false);
        }
    }//GEN-LAST:event_btnUltimoActionPerformed

